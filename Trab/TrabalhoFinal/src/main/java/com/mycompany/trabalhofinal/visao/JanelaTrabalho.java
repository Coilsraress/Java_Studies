    private void remover_clienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_remover_clienteActionPerformed
    String nomeClienteSelecionado = (String) clientes.getSelectedItem();
    Cliente clienteSelecionado = locadora.getManipulaClientes().getClientes().stream()
            .filter(cliente -> cliente.getNome().equals(nomeClienteSelecionado))
            .findFirst()
            .orElse(null);

    if (clienteSelecionado != null) {
        locadora.removerCliente(clienteSelecionado);
        atualizarComboBoxClientes(); // Atualiza a JComboBox após remover um cliente
        // Limpa os campos de texto
        nome_cliente.setText("");
        // Limpe outros campos se necessário
        JOptionPane.showMessageDialog(this, "Cliente removido com sucesso!");
    } else {
        JOptionPane.showMessageDialog(this, "Nenhum cliente selecionado para remover.");
    }
       
    }//GEN-LAST:event_remover_clienteActionPerformed

