    private void filmes1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_filmes1ActionPerformed
        String nomeFilmeSelecionado = (String) filmes1.getSelectedItem();
    Filme filmeSelecionado = locadora.getManipulaFilmes().getFilmePorNome(nomeFilmeSelecionado);

    if (filmeSelecionado != null) {
        // Limpa a área de texto antes de exibir os novos dados
        clientes_datas.setText("");

        // Exibe os detalhes dos clientes que alugaram o filme
        StringBuilder detalhesClientes = new StringBuilder();
        for (Cliente cliente : locadora.getManipulaClientes().getClientes()) {
            if (cliente.getFilmeAlugado() != null && cliente.getFilmeAlugado().getNome().equals(nomeFilmeSelecionado)) {
                detalhesClientes.append("Nome: ").append(cliente.getNome())
                                .append(", ID: ").append(cliente.getId())
                                .append(", Data de Devolução: ").append(cliente.getDataDevolucao())
                                .append("\n");
            }
        }

        // Atualiza a área de texto com os detalhes dos clientes
        if (detalhesClientes.length() > 0) {
            clientes_datas.setText(detalhesClientes.toString());
        } else {
            clientes_datas.setText("Nenhum cliente alugou este filme.");
        }
    }
    }//GEN-LAST:event_filmes1ActionPerformed

