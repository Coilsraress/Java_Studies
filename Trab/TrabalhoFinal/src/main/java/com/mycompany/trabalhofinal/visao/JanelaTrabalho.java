    private void alugarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_alugarActionPerformed
    String nomeFilmeSelecionado = (String) filmes.getSelectedItem();
    String nomeClienteSelecionado = (String) clientes.getSelectedItem();
    String dataDevolucao = JOptionPane.showInputDialog(this, "Digite a data de devolução:");

    if (nomeFilmeSelecionado != null && nomeClienteSelecionado != null && dataDevolucao != null) {
        Filme filmeSelecionado = locadora.getManipulaFilmes().getFilmePorNome(nomeFilmeSelecionado);
        Cliente clienteSelecionado = locadora.getManipulaClientes().getClientePorNome(nomeClienteSelecionado);

        if (filmeSelecionado != null && clienteSelecionado != null && filmeSelecionado.getUnidades() > 0) {
            locadora.alugarFilme(clienteSelecionado, filmeSelecionado, dataDevolucao);
            JOptionPane.showMessageDialog(this, "Filme alugado com sucesso!");
            atualizarComboBoxFilmesAlugados(); // Atualiza a JComboBox de filmes
        } else {
            JOptionPane.showMessageDialog(this, "Não há unidades disponíveis ou cliente não encontrado.");
        }
    } else {
        JOptionPane.showMessageDialog(this, "Selecione um filme e um cliente.");
    }
    locadora.getManipulaClientes().salvarClientes();
    locadora.getManipulaFilmes().salvarFilmes();
    }//GEN-LAST:event_alugarActionPerformed

    private void remove_catActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_remove_catActionPerformed
         String nomeFilmeSelecionado = (String) filmes.getSelectedItem();
    Filme filmeSelecionado = locadora.getManipulaFilmes().getFilmePorNome(nomeFilmeSelecionado);

    if (filmeSelecionado != null) {
        // Remove o filme da locadora
        locadora.getManipulaFilmes().removerFilme(filmeSelecionado);
        
        // Atualiza a JComboBox para refletir a remoção
        atualizarComboBoxFilmes();
        atualizarComboBoxFilmesAlugados();
        
        // Limpa os campos de texto
        genero_filme.setText("");
        ano_filme.setText("");
        unidades_filme.setText("");
        sinopse_filme.setText("");
        
        // Exibe uma mensagem de confirmação
        JOptionPane.showMessageDialog(this, "Filme removido do catálogo com sucesso!");
    } else {
        // Exibe uma mensagem de erro se nenhum filme estiver selecionado
        JOptionPane.showMessageDialog(this, "Nenhum filme selecionado para remover.");
    }
    }//GEN-LAST:event_remove_catActionPerformed

